#!/bin/sh

prog=schurOneMPAlattice_socp_slb_bandpass_test.m
depends="schurOneMPAlattice_socp_slb_bandpass_test.m test_common.m \
schurOneMPAlatticeAsq.m \
schurOneMPAlatticeT.m \
schurOneMPAlatticeP.m \
