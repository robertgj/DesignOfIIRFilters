cat > test_A2k_coef.m << 'EOF'
A2k = [  -0.7696779513,   0.7233437079,   0.4465398111,  -0.5760186160, ... 
          0.7314429046,  -0.2540400916,  -0.0688779304,   0.3764937513, ... 
         -0.2341109017,   0.1053786835 ];
